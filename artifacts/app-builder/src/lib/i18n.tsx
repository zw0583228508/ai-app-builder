// ── Context ──────────────────────────────────────────────────────────────────
interface LangContextValue {
  lang: LangCode;
  meta: LangMeta;
  setLang: (l: LangCode) => void;
  t: (key: TranslationKey) => string;
}

const LangContext = createContext<LangContextValue | null>(null);

const STORAGE_KEY = "ai-builder-lang";
const DEFAULT_LANG: LangCode = "he";

function safeGetStorage(key: string): string | null {
  try {
    return localStorage.getItem(key);
  } catch {
    return null;
  }
}

function safeSetStorage(key: string, value: string): void {
  try {
    localStorage.setItem(key, value);
  } catch {}
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>(() => {
    // 1. Explicit user choice stored in localStorage
    const stored = safeGetStorage(STORAGE_KEY) as LangCode | null;
    if (stored && T[stored]) return stored;
    // 2. Fall back to app default (Hebrew — this is a Hebrew-first app)
    return DEFAULT_LANG;
  });

  const meta = LANGUAGES.find((l) => l.code === lang)!;

  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = meta.rtl ? "rtl" : "ltr";
  }, [lang, meta]);

  const setLang = (l: LangCode) => {
    safeSetStorage(STORAGE_KEY, l);
    setLangState(l);
  };

  const t = (key: TranslationKey): string =>
    T[lang][key] ?? T[DEFAULT_LANG][key] ?? key;

  return (
    <LangContext.Provider value={{ lang, meta, setLang, t }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useLang must be used inside LanguageProvider");
  return ctx;
}
